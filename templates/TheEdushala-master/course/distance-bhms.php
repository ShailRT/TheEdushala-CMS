											<p id="bba-career" class="mt-5">
											<strong>Career Prospects </strong>
											<p>After graduation, students with a Bachelor of Naturopathy & Yoga Sciences (BHMS) have a wide range of job options. The student's chances of employment and success are determined by the type of vocation they pick, which will vary depending on their speciality. Self-employed Naturopaths typically earn more money by opening their own clinics and businesses. Graduates of BHMS who work in government hospitals earn well as well. While working in the private sector as a BHMS graduate can be stressful, there are more options for higher pay and advancement. Naturopathic doctors are also in high demand in places like the Gulf area and the Middle East..</p>
										</p>


















										<p>
											<strong id="salary-job-bba">Salary</strong>
											<p>Naturopathy is a rapidly expanding field, and many graduates go on to create their own practises following graduation. Naturopaths who work for themselves can expect to make between Rs.10,000 and Rs.20,000 per month. In the private sector, more experienced workers may earn more, with a monthly compensation of roughly Rs.1 lakh. B.N.Y.S doctors and specialists are also hired by the government, and their salaries range from Rs. 15,600 to Rs. 39,100, with a Rs. 5400 grade pay. Students who practise in other countries may have a better chance of earning more money with their BHMS degree.</p>
